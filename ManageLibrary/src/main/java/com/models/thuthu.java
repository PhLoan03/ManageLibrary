package com.models;

public class thuthu {
	private String mathuthu;
	private String tenthuthu;
	private String diachi;
	private String sdt;
	private String email;
	private String pass;
	public thuthu() {
		super();
	}
	public thuthu(String mathuthu, String tenthuthu, String diachi, String sdt, String email, String pass) {
		super();
		this.mathuthu = mathuthu;
		this.tenthuthu = tenthuthu;
		this.diachi = diachi;
		this.sdt = sdt;
		this.email = email;
		this.pass = pass;
	}
	public String getMathuthu() {
		return mathuthu;
	}
	public void setMathuthu(String mathuthu) {
		this.mathuthu = mathuthu;
	}
	public String getTenthuthu() {
		return tenthuthu;
	}
	public void setTenthuthu(String tenthuthu) {
		this.tenthuthu = tenthuthu;
	}
	public String getDiachi() {
		return diachi;
	}
	public void setDiachi(String diachi) {
		this.diachi = diachi;
	}
	public String getSdt() {
		return sdt;
	}
	public void setSdt(String sdt) {
		this.sdt = sdt;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPass() {
		return pass;
	}
	public void setPass(String pass) {
		this.pass = pass;
	}
	
	
}
